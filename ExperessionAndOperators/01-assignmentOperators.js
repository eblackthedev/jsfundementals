// Assingment Operators

let r = 5;
console.log(r);
// our equal sign is the assignment

// Addition Assigment
r += 1; // same as r = r + 1 or, 5 + 1 in this case
console.log(r);

// Subtraction Assignment
r -= 2; //r = r - 2
console.log(r);

// Multiplecation Assignment
r *= 2; // r = r * 2
console.log(r);

//Division Assingment
r /= 1; // r = r / 1
console.log(r);

// Exponential Assignment
r **= 3; // r = r * r
console.log(r);

//Remainder (modulus) Assignment
r %= 1; // r = r % 1
console.log(r);

let y = 5;
console.log(y);

y %= 2;
console.log(y);
